    def check_and_repair_database(self, db_path):
        """‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÅ‡∏•‡∏∞‡∏ã‡πà‡∏≠‡∏°‡πÅ‡∏ã‡∏°‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•"""
        try:
            conn = sqlite3.connect(db_path)
            cursor = conn.cursor()
            
            # ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏°‡∏ö‡∏π‡∏£‡∏ì‡πå
            cursor.execute("PRAGMA integrity_check")
            result = cursor.fetchone()
            
            if result[0] == "ok":
                print(f"‚úÖ ‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• {db_path} ‡∏™‡∏°‡∏ö‡∏π‡∏£‡∏ì‡πå")
                
                # ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ï‡∏≤‡∏£‡∏≤‡∏á
                cursor.execute("SELECT name FROM sqlite_master WHERE type='table'")
                tables = cursor.fetchall()
                
                required_tables = ['bird_activity', 'anomaly_detection']
                existing_tables = [table[0] for table in tables]
                
                missing_tables = []
                for required in required_tables:
                    if required not in existing_tables:
                        missing_tables.append(required)
                
                if missing_tables:
                    print(f"‚ö†Ô∏è ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏ó‡∏µ‡πà‡∏´‡∏≤‡∏¢‡πÑ‡∏õ: {missing_tables}")
                    # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏ó‡∏µ‡πà‡∏´‡∏≤‡∏¢‡πÑ‡∏õ
                    if 'bird_activity' in missing_tables:
                        cursor.execute('''
                        CREATE TABLE bird_activity (
                            id INTEGER PRIMARY KEY AUTOINCREMENT,
                            timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
                            birds_in INTEGER DEFAULT 0,
                            birds_out INTEGER DEFAULT 0,
                            confidence TEXT,
                            weather_data TEXT,
                            meta_data TEXT
                        )
                        ''')
                        print("‚úÖ ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á bird_activity")
                    
                    if 'anomaly_detection' in missing_tables:
                        cursor.execute('''
                        CREATE TABLE anomaly_detection (
                            id INTEGER PRIMARY KEY AUTOINCREMENT,
                            timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
                            object_type TEXT NOT NULL,
                            confidence TEXT NOT NULL,
                            image_path TEXT,
                            status TEXT DEFAULT 'new'
                        )
                        ''')
                        print("‚úÖ ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á anomaly_detection")
                    
                    conn.commit()
                
                conn.close()
                return True
            else:
                print(f"‚ùå ‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• {db_path} ‡πÄ‡∏™‡∏µ‡∏¢‡∏´‡∏≤‡∏¢: {result[0]}")
                conn.close()
                return False
                
        except Exception as e:
            print(f"‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ: {e}")
