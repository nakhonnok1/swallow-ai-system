class DatabaseManager:
    """‡∏£‡∏∞‡∏ö‡∏ö‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•"""
    
    def __init__(self):
        self.current_dir = Path(__file__).parent
        self.backup_dir = self.current_dir / "backups" 
        self.backup_dir.mkdir(exist_ok=True)
        
    def backup_database(self, db_path):
        """‡∏™‡∏≥‡∏£‡∏≠‡∏á‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•"""
        if not os.path.exists(db_path):
            print(f"‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö‡πÑ‡∏ü‡∏•‡πå: {db_path}")
            return False
            
        try:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            db_name = Path(db_path).stem
            backup_name = f"{db_name}_backup_{timestamp}.db"
            backup_path = self.backup_dir / backup_name
            
            shutil.copy2(db_path, backup_path)
            print(f"‚úÖ ‡∏™‡∏≥‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢: {backup_name}")
            return True
            
        except Exception as e:
            print(f"‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏™‡∏≥‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ: {e}")
            return False
    
    def reset_database(self, db_path, create_tables=True):
        """‡∏£‡∏µ‡πÄ‡∏ã‡πá‡∏ï‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•"""
        try:
            # ‡∏™‡∏≥‡∏£‡∏≠‡∏á‡∏Å‡πà‡∏≠‡∏ô‡∏•‡∏ö
            if os.path.exists(db_path):
                self.backup_database(db_path)
                os.remove(db_path)
                print(f"üóëÔ∏è ‡∏•‡∏ö‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏Å‡πà‡∏≤: {db_path}")
            
            if create_tables:
                # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà
                conn = sqlite3.connect(db_path)
                cursor = conn.cursor()
                
                # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á bird_activity
                cursor.execute('''
                CREATE TABLE IF NOT EXISTS bird_activity (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
                    birds_in INTEGER DEFAULT 0,
                    birds_out INTEGER DEFAULT 0,
                    confidence TEXT,
                    weather_data TEXT,
                    meta_data TEXT
                )
                ''')
                
                # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á anomaly_detection
                cursor.execute('''
                CREATE TABLE IF NOT EXISTS anomaly_detection (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
                    object_type TEXT NOT NULL,
                    confidence TEXT NOT NULL,
                    image_path TEXT,
                    status TEXT DEFAULT 'new'
                )
                ''')
                
                # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á system_stats
                cursor.execute('''
                CREATE TABLE IF NOT EXISTS system_stats (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
                    total_birds_today INTEGER DEFAULT 0,
                    peak_activity_hour INTEGER DEFAULT 0,
                    anomalies_detected INTEGER DEFAULT 0,
                    system_uptime TEXT,
                    performance_score REAL DEFAULT 0.0
                )
                ''')
                
                conn.commit()
                conn.close()
                print(f"‚úÖ ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà: {db_path}")
                
            return True
            
        except Exception as e:
            print(f"‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏£‡∏µ‡πÄ‡∏ã‡πá‡∏ï‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ: {e}")
